interface LanguageContextProps {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string, values?: Record<string, string | number>) => string;
}

const LanguageContext = createContext<LanguageContextProps>({
  language: "en",
  setLanguage: () => {},
  t: key => translations.en[key] || key,
});

export const LanguageProvider = ({
  children,
}: {
  children: React.ReactNode;
}) => {
  const [language, setLanguageState] = useState<Language>("en");
  const [liveStrings, setLiveStrings] = useState<Record<Language, TranslationMap>>({
    en: {},
    ny: {},
  });

  React.useEffect(() => {
    const saved = window.localStorage.getItem("dlem_lang");
    if (saved === "ny" || saved === "en") {
      setLanguageState(saved);
    }
    fetchContentStrings()
      .then((rows) => {
        if (!Array.isArray(rows)) return;
        const next: Record<Language, TranslationMap> = { en: {}, ny: {} };
        rows.forEach((row: { key: string; english: string; chichewa: string }) => {
          next.en[row.key] = row.english;
          next.ny[row.key] = row.chichewa;
        });
        setLiveStrings(next);
      })
      .catch(() => undefined);
  }, []);

  React.useEffect(() => {
    document.documentElement.lang = language === "ny" ? "ny" : "en";
  }, [language]);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    window.localStorage.setItem("dlem_lang", lang);
  };

  const t = (
    key: string,
    values: Record<string, string | number> = {},
  ): string => {
    const fallback = values.default ? String(values.default) : key;
    const englishTemplate = liveStrings.en[key] || translations.en[key] || fallback;
    let template =
      language === "ny"
        ? liveStrings.ny[key] || translations.ny[key] || englishTemplate
        : englishTemplate;
    Object.entries(values).forEach(([name, value]) => {
      template = template.replace(`{${name}}`, String(value));
    });
    return template;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => useContext(LanguageContext);

